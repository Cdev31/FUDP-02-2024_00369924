#include <iostream>

using namespace std;

int main()
{
    int number1 = 23, number2 = 28;

    //its making process and print sum result
    cout << "your sum are: " << number1 + number2 << endl;

    //its making process and print remainder result
    cout << "your remainder are: " << number1 - number2 << endl;

    //its making process and print multiplication result
    cout << "your multiplication are: " << number1 * number2 << endl;

    //its making process and print division result
    cout << "your division are: " << (float)number1 / (float)number2 << endl;
    
    return 0;
}
